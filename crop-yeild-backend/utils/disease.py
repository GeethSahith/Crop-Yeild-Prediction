def assign_risk(disease_name):
    name = disease_name.lower()

    if 'healthy' in name:
        return 'Low'
    if 'virus' in name or 'bacterial' in name:
        return 'High'
    if 'blight' in name or 'rot' in name or 'spot' in name or 'rust' in name or 'mildew' in name or 'scab' in name:
        return 'Medium'
    return 'Medium'


